function getNestedValue(source, path) {
  return path.split(".").reduce((value, part) => value?.[part], source);
}

export function translate(key, language = "ru", params = {}) {
  const value =
    getNestedValue(dictionaries[language], key) ??
    getNestedValue(dictionaries.en, key) ??
    key;

  if (typeof value !== "string") return key;

  return Object.entries(params).reduce(
    (text, [paramKey, paramValue]) =>
      text.replaceAll(`{${paramKey}}`, String(paramValue)),
    value,
  );
}

function normalizeKey(value) {
  return String(value || "")
    .trim()
    .toLowerCase();
}

function translateMapped(group, map, value, language) {
  const key = map[normalizeKey(value)];
  if (!key) return value || translate("common.unknown", language);
  return translate(`${group}.${key}`, language);
}

